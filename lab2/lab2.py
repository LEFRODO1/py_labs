for i in range(100,10000):
    if (i > 1000):
        h =(i // 1000 % 10) **4
        a =(i // 100 % 10) **4
        b =(i // 10 % 10) **4
        c =(i % 10) **4
    else:
        h = 0
        a =(i // 100 % 10) **3
        b =(i // 10 % 10) **3
        c =(i % 10) **3

    if h+a+b+c == i:
        print(i)